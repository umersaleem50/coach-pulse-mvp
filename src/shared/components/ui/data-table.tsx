import { flexRender } from "@tanstack/react-table";

import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from "./table";

import { useDataTable } from "@/shared/hooks/useDataTable";
import { DataTablePagination } from "../DataTablePagination";

// interface DataTableProps<TData, TValue> {
